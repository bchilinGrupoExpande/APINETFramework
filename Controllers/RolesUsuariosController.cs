using System;
using System.Collections.Generic;
using System.Linq;
using System.Net;
using System.Net.Http;
using System.Web.Http;
using APIPresupuestos.Models;
using ConectarDatos;

namespace APIPresupuestos.Controllers
{
    public class RolesUsuariosController : BaseController
    {
        [HttpPost]
        public Reply Agregar([FromBody] RolesUsuariosViewModel model)
        {
            Reply oR = new Reply();
            oR.result = 0;

            if (!Verify(model.token, model.IDUsuario))
            {
                oR.message = "No autorizado, idenfiquese para realizar las peticiones";
                return oR;
            }
            try
            {
                using (PresupuestosPruebaEntities db = new PresupuestosPruebaEntities())
                {
                    roles_usuarios oRolesUsuarios = new roles_usuarios();

                    oRolesUsuarios.nombre = model.nombre;
                    oRolesUsuarios.descripcion = model.descripcion;
                    oRolesUsuarios.estado = 1;
                    db.roles_usuarios.Add(oRolesUsuarios);
                    db.SaveChanges();


                    oR.data = "[]";
                    oR.result = 1;

                }
            }
            catch (Exception ex)
            {
                oR.message = "Ocurrió un error en el servidor, intenta más tarde";
            }
            return oR;
        }

        [HttpPut]
        public Reply Editar([FromBody] RolesUsuariosViewModel model)
        {
            Reply oR = new Reply();
            oR.result = 0;
            if (!Verify(model.token, model.IDUsuario))
            {
                oR.message = "No autorizado, idenfiquese para realizar las peticiones";
                return oR;
            }
            try
            {
                using (PresupuestosPruebaEntities db = new PresupuestosPruebaEntities())
                {
                    roles_usuarios oRolesUsuarios = db.roles_usuarios.Find(model.id);

                    oRolesUsuarios.nombre = model.nombre;
                    oRolesUsuarios.descripcion = model.descripcion;
                    oRolesUsuarios.estado = 1;

                    db.Entry(oRolesUsuarios).State = System.Data.Entity.EntityState.Modified;
                    db.SaveChanges();

                    oR.data = "[]";
                    oR.result = 1;

                }
            }
            catch (Exception ex)
            {
                oR.message = "Ocurrió un error en el servidor, intenta más tarde";
            }
            return oR;
        }

    }
}
